export const useTranslation = () => {
  const lang = useLanguageStore((s) => s.language);

  const t = useMemo(
    () => (key: string) => translations[lang]?.[key] ?? translations.uk[key] ?? key,
    [lang],
  );

  return { t, lang };
};
